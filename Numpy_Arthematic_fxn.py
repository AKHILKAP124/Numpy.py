# Arthematic Functions in Numpy
import numpy as np
a = np.arange(1,11).reshape(2,5)

# Multipication Function
Mutli_arr = a * a

# Addition Function
Add_arr = a + a

# Subtration Function
Sub_arr = a - a

# Division Function
Div_arr = a / a

# Remainder Function 
Rem_arr = a % a

# Print All Functions
print("Array 'a' is =",a)
print(" ")
print("Multication of Array a to Array a =",Mutli_arr)
print(" ")
print("Addition of Array a to Array a =",Add_arr)
print(" ")
print("Substration of Array a to Array a =",Sub_arr)
print(" ")
print("Division of Array a to Array a =",Div_arr)
print(" ")
print("Remainder of Array a to Array a =",Rem_arr)